print('if statement')
a=5
if a<10:
    print('a is less than 10')

print('if else statement')
b=7
if b>10:
    print('b is greater than 10')
else:
    print('b is less than 10')

print('elif statement')
c=9
if c<9:
    print('c is less than 9')
elif c==9:
    print('c is equal to 9')
else:
    print('c is greater than 9')
